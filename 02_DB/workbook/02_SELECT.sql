-- 1번
SELECT STUDENT_NO "학번", STUDENT_NAME "이름",
TO_CHAR(ENTRANCE_DATE,'YYYY-MM-DD')  "입학년도"
FROM TB_STUDENT
NATURAL JOIN TB_DEPARTMENT
WHERE DEPARTMENT_NO ='002'
ORDER BY ENTRANCE_DATE;

-- 2번
SELECT PROFESSOR_NAME , PROFESSOR_SSN 
FROM TB_PROFESSOR
WHERE LENGTH(PROFESSOR_NAME) !=3;

-- 3번
SELECT PROFESSOR_NAME, PROFESSOR_SSN , 
FLOOR(MONTHS_BETWEEN(SYSDATE,TO_DATE('19'||SUBSTR(PROFESSOR_SSN,1,6)))/12) 나이
FROM TB_PROFESSOR
WHERE SUBSTR(PROFESSOR_SSN,8,1)='1'
ORDER BY 나이;

-- 4번
SELECT TRIM(LEADING SUBSTR(PROFESSOR_NAME,1,1) FROM PROFESSOR_NAME) 이름
FROM TB_PROFESSOR;

-- 5번
SELECT STUDENT_NO , STUDENT_NAME
FROM TB_STUDENT
WHERE FLOOR(MONTHS_BETWEEN(ENTRANCE_DATE,TO_DATE('19'||SUBSTR(STUDENT_SSN,1,6)))/12)=19;

